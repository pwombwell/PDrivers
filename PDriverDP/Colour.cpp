MyError colour_rgbto256colpixval(uint32_t bbGGRR00, uint32_t& col, JobWS& job)
{
    PDumperCall call = {0};
    call.r0 = bbGGRR00;
    call.r2 = job.config().stripType();
    call.r3 = (uint32_t)(uintptr_t)&job.config().pdumperWord();
    call.r4 = job.output().halftoneWord();

    MyError err = CallPDumperForJob(job,
                                    PDumperReason_ColourSet,
                                    call);
    if (err)
        return err;

    col = call.r3;

    return nullptr;
}

uint8_t colour_rgbtopixval(uint32_t bbGGRR00, CoreJobWS& coreJob)
{
    JobWS& job = toJobWS(coreJob);
    uint32_t col = 0;

#if Libra1
    if (job.config().stripType() == 4 || job.config().stripType() == 5) {
        (void)ColourTrans::returnGCOLForMode(bbGGRR00, 21, 0, col);

        return (uint8_t)col;
    }
#endif

    (void)colour_rgbto256colpixval(bbGGRR00, col, job);
    return (uint8_t)col;
}

MyError colour_rgbtopixvalwide(uint32_t bbGGRR00,
                            uint32_t& value,
                            uint8_t& bytes,
                            CoreJobWS& coreJob)
{
    JobWS& job = toJobWS(coreJob);

#if Libra1
    if (job.config().stripType() == 4 || job.config().stripType() == 5) {
        uint32_t mode_word = 0;
        if (job.config().stripType() == 4) {
            mode_word = (SpriteType_New16bpp << 27);
            bytes = 2;
        } else {
            mode_word = (SpriteType_New32bpp << 27);
            bytes = 4;
        }
        mode_word |= (90u << 14);
        mode_word |= (90u << 1);
        mode_word |= 1u;

        return ColourTrans::returnGCOLForMode(bbGGRR00, mode_word, 0, value);
    }
#endif

    MyError err = colour_rgbto256colpixval(bbGGRR00, value, job);
    if (err)
        return err;

    value &= 0xFFu;
    bytes = 1;

    return nullptr;
}

MyError colour_setrealrgb(uint32_t bbGGRR00, CoreJobWS& coreJob)
{
    MyError err;
    JobWS& job = toJobWS(coreJob);
    uint32_t pixval;

#if Libra1
    if (job.config().stripType() == 4 || job.config().stripType() == 5) {
        if ((err = colour_rgbto256colpixval(bbGGRR00, pixval, job)) != nullptr)
            return err;

        return XOS_SetColour(0, pixval);
    }
#endif

    if ((err = colour_rgbto256colpixval(bbGGRR00, pixval, job)) != nullptr)
        return err;

#if NbppBufferOK
    if (job.config().outputBpp() == 8) {
        MyError err;
#if Libra1
        if ((err = XOS_SetColour(0, pixval)) != nullptr)
            return err;
#else
        uint32_t r1 = pixval;
        uint32_t gcol = (r1 & 0x87u) | ((r1 & 0x70u) >> 1) | ((r1 & 0x08u) << 3);
        if ((err = vdu_pair(ws, 18)) != nullptr)
            return err;

        if ((err = vdu_char(ws, (uint8_t)(gcol >> 2))) != nullptr)
            return err;
#endif
        if ((err = vdu_counted_string(colour_foretint)) != nullptr)
            return err;

        if ((err = vdu_char((uint8_t)((pixval << 6) & 0xFFu))) != nullptr)
            return err;

        if ((err = vdu_pair(0)) != nullptr)
            return err;

        if ((err = vdu_pair(0)) != nullptr)
            return err;

        return vdu_pair(0);
    }
#else
    (void)jobWS;
#endif

    const uint8_t *table = nullptr;
    uint32_t idx = 0;
    if (job.output().halftoneX() == 4) {
        table = fourbyfourECFbytes;
        idx = (pixval >> 8) & 0xFFu;
    } else {
        table = eightbyeightECFbytes;
        idx = (pixval >> 16) & 0xFFu;
    }

    const uint8_t *entry = table + (idx * 8u);
    if ((err = vdu_counted_string(setcolourandECF)) != nullptr)
        return err;

    uint32_t word0 = (uint32_t)entry[0] | ((uint32_t)entry[1] << 8) |
                     ((uint32_t)entry[2] << 16) | ((uint32_t)entry[3] << 24);
    uint32_t word1 = (uint32_t)entry[4] | ((uint32_t)entry[5] << 8) |
                     ((uint32_t)entry[6] << 16) | ((uint32_t)entry[7] << 24);

    if ((err = vdu_pair(word0 & 0xFFFFu)) != nullptr)
        return err;
    if ((err = vdu_pair((word0 >> 16) & 0xFFFFu)) != nullptr)
        return err;
    if ((err = vdu_pair(word1 & 0xFFFFu)) != nullptr)
        return err;
    return vdu_pair((word1 >> 16) & 0xFFFFu);
}
